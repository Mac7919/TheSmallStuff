nil = 0
num = 0
max = 1
cap = 'A'
low = 'a'

#Numeric and character equality comparisons
print('Equality :	', nil, '==', num, nil == num)
print('Equality :	', cap, '==', low, cap == low)

#inequality comparison
print('Inequality :	', nil, '!=', max, nil != max)

#greater and lesser comparisons
print('Greater :	', nil, '>', max, nil > max)
print('Lesser :	', nil, '<', max, nil < max)

#greater or equal and lesser or equal comparisons
print ('More Or Equal :	', nil, '>=', num, nil >= num)
print ('Less Or Equal :	', max, '<=', num, max <= num)
