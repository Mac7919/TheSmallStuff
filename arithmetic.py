# Doing some simple arithmetic
a = 8
b = 2

#Using the + Operator
print('Addition:\t', a, '+', b, '=', a + b)

#Using the - Operator
print ('Subtract:\t', a, '-', b, '=', a - b)

#Using the * Operator
print('Multiplication:\t', a, '*', b, '=', a * b)

#Using the % Operator
print('Modulus:\t', a, '%', b, '=', a % b)

#Using the // (float) and / (integer) operator
print('Division:\t', a, '//', b, '=', a//b)
print('Floor Division:\t', a,'/', b, '=', a/b)

#Using the ** operator
print('Exponent:\t', a, '**', b, '=', a ** b)
